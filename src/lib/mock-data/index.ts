/** Get client by ID */
export const getClientById = (id: string): Client | undefined =>
  mockClients.find((c) => c.id === id);

/** Get matter by ID */
export const getMatterById = (id: string): Matter | undefined =>
  mockMatters.find((m) => m.id === id);

/** Get all matters for a client */
export const getMattersByClientId = (clientId: string): Matter[] =>
  mockMatters.filter((m) => m.clientId === clientId);

/** Get all hearings for a matter */
export const getHearingsByMatterId = (matterId: string): Hearing[] =>
  mockHearings.filter((h) => h.matterId === matterId);

/** Get upcoming hearings (sorted by date ascending) */
export const getUpcomingHearings = (): Hearing[] =>
  mockHearings
    .filter((h) => h.status === 'upcoming')
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());

/** Get hearings for a specific date (YYYY-MM-DD) */
export const getHearingsForDate = (date: string): Hearing[] =>
  mockHearings.filter((h) => h.date === date);

/** Get all fee entries for a matter */
export const getFeeEntriesByMatterId = (matterId: string): FeeEntry[] =>
  mockFeeEntries.filter((f) => f.matterId === matterId);

/** Get all payments for a matter */
export const getPaymentsByMatterId = (matterId: string): Payment[] =>
  mockPayments.filter((p) => p.matterId === matterId);

/** Get all expenses for a matter */
export const getExpensesByMatterId = (matterId: string): Expense[] =>
  mockExpenses.filter((e) => e.matterId === matterId);

/** Get all documents for a matter */
export const getDocumentsByMatterId = (matterId: string): Document[] =>
  mockDocuments.filter((d) => d.matterId === matterId);

/** Get all tasks assigned to a user */
export const getTasksByAssignee = (userId: string): Task[] =>
  mockTasks.filter((t) => t.assignedTo === userId);

/** Get all pending tasks */
export const getPendingTasks = (): Task[] =>
  mockTasks.filter((t) => t.status === 'pending' || t.status === 'in_progress');

/** Get user by ID */
export const getUserById = (id: string): User | undefined =>
  mockUsers.find((u) => u.id === id);

/** Get overdue fee entries */
export const getOverdueFeeEntries = (): FeeEntry[] =>
  mockFeeEntries.filter((f) => f.status === 'overdue');

/** Get timeline for an entity */
export const getTimelineByEntityId = (entityId: string): TimelineEntry[] =>
  mockTimeline
    .filter((t) => t.entityId === entityId)
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());

/** Calculate total outstanding across all clients */
export const getTotalOutstanding = (): number =>
  mockClients.reduce((sum, c) => sum + c.totalOutstanding, 0);

/** Search matters by title or case number */
export const searchMatters = (query: string): Matter[] => {
  const q = query.toLowerCase();
  return mockMatters.filter(
    (m) =>
      m.matterTitle.toLowerCase().includes(q) ||
      m.caseNumber?.toLowerCase().includes(q) ||
      m.cnrNumber?.toLowerCase().includes(q) ||
      m.oppositeParty?.toLowerCase().includes(q)
  );
};

/** Search clients by name, mobile, or email */
export const searchClients = (query: string): Client[] => {
  const q = query.toLowerCase();
  return mockClients.filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.mobile.includes(q) ||
      c.email?.toLowerCase().includes(q)
  );
};
